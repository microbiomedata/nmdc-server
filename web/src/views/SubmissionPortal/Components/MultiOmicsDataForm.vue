<script setup lang="ts">
import { computed, nextTick, useTemplateRef, watch } from 'vue';

import Definitions from '@/definitions';
import doiProviderValues from '@/schema';
import { AwardTypes, JGI_TEMPLATE_NAMES } from '@/views/SubmissionPortal/types';

import SubmissionDocsLink from './SubmissionDocsLink.vue';
import DataTypes from './DataTypes.vue';
import DoeFacility from './DoeFacility.vue';
import PageTitle from '@/components/Presentation/PageTitle.vue';
import SubmissionForm from '@/views/SubmissionPortal/Components/SubmissionForm.vue';
import SubmissionUneditableBanner from '@/views/SubmissionPortal/Components/SubmissionUneditableBanner.vue';
import { checkDoiFormat } from '@/views/SubmissionPortal/utils.ts';
import { useSubmissionStore } from '../store';

const OTHER = 'OTHER';

const store = useSubmissionStore();
const { templateHasData } = store;
const multiOmicsForm = computed(() => store.sampleSet.forms.multiOmicsForm);

const formRef = useTemplateRef<InstanceType<typeof SubmissionForm>>('formRef');
const selectedFacilities = computed(() => multiOmicsForm.value.facilities ?? []);

const projectAwardValidationRules = () => [(v: string | undefined) => {
  const facilityChosen = selectedFacilities.value.length > 0;
  if (!facilityChosen) {
    return true;
  }
  const awardChosen = v !== undefined;
  if (!awardChosen) {
    return 'If submitting to a use facility, this field is required.';
  }
  return true;
}];
const otherAwardValidationRules = () => [(v: string | undefined) => {
  const facilityChosen = selectedFacilities.value.length > 0;
  const awardChosen = multiOmicsForm.value.award !== undefined;
  if (!facilityChosen && !awardChosen) {
    return true;
  }
  if (!awardChosen || multiOmicsForm.value.award !== OTHER) {
    return true;
  }
  const inputEmpty = v === undefined || v.trim().length === 0;
  if (inputEmpty) {
    return 'Please enter the kind of project award.';
  }
  return true;
}];

const doiRequired = computed(() => (
  (multiOmicsForm.value.dataGenerated && multiOmicsForm.value.facilityGenerated)
  || (!multiOmicsForm.value.dataGenerated && !multiOmicsForm.value.unknownDoi)
));

const doiValueRules = (i: number) => (
  [
    (value: string) => {
      const valid = !!value || !doiRequired.value;
      return valid || 'An award DOI is required if data was or is going to be generated by a DOE facility.';
    },
    (value: string) => {
      const hasProvider = multiOmicsForm.value.awardDois && multiOmicsForm.value.awardDois[i] && multiOmicsForm.value.awardDois[i].provider;
      let valid = true;
      if (hasProvider) {
        valid = !!value;
      }
      return valid || 'DOI must be provided if a provider is selected.';
    },
    (value: string) => {
      return !value || checkDoiFormat(value);
    },
  ]
);

const doiProviderRules = (i: number) => (
  [
    (provider: string) => {
      const hasValue = multiOmicsForm.value.awardDois && multiOmicsForm.value.awardDois[i] && multiOmicsForm.value.awardDois[i].value !== '';
      let valid = true;
      if (hasValue || doiRequired.value) {
        valid = !!provider;
      }
      return valid || 'A provider must be selected.';
    },
  ]
);

function addAwardDoi() {
  if (!Array.isArray(multiOmicsForm.value.awardDois)) {
    multiOmicsForm.value.awardDois = [];
  }
  multiOmicsForm.value.awardDois.push({
    value: '',
    provider: null,
  });
}

function removeAwardDoi(i: number) {
  if (multiOmicsForm.value.awardDois === null) {
    multiOmicsForm.value.awardDois = [];
  }
  const numFacilities = multiOmicsForm.value.facilities?.length || 0;
  if ((numFacilities < multiOmicsForm.value.awardDois.length && !multiOmicsForm.value.dataGenerated) || (multiOmicsForm.value.facilityGenerated && multiOmicsForm.value.dataGenerated && multiOmicsForm.value.awardDois.length > 1) || (!multiOmicsForm.value.facilityGenerated && multiOmicsForm.value.dataGenerated)) {
    multiOmicsForm.value.awardDois.splice(i, 1);
  }
}

const revalidate = () => {
  nextTick(() => void formRef.value?.validate());
};

watch(
  () => multiOmicsForm.value.award,
  (award) => {
    const awardTypes = Object.values(AwardTypes) as string[];
    if (award && awardTypes.includes(award)) {
      multiOmicsForm.value.otherAward = '';
    }
    revalidate();
  },
);
</script>

<template>
  <div>
    <SubmissionUneditableBanner
      :allowed-roles="['owner', 'editor']"
      in-sample-set-context
      edge-to-edge
    />
    <PageTitle
      title="Multi-omics Data"
      subtitle="Information about the type of samples being submitted."
    >
      <template #help>
        <submission-docs-link anchor="multi-omics-data" />
      </template>
    </PageTitle>
    <SubmissionForm
      ref="formRef"
      in-sample-set-context
      @valid-state-changed="(state) => multiOmicsForm.validation = state"
    >
      <v-radio-group
        v-model="multiOmicsForm.dataGenerated"
        label="Have data already been generated for your study? *"
        :rules="[v => (v === true || v === false) || 'You must select if data has been generated.']"
        :disabled="templateHasData(JGI_TEMPLATE_NAMES) || templateHasData('emsl') || undefined"
        @change="revalidate"
      >
        <v-radio
          label="No"
          :value="false"
        />
        <v-radio
          label="Yes"
          :value="true"
        />
      </v-radio-group>
      <v-radio-group
        v-if="multiOmicsForm.dataGenerated"
        v-model="multiOmicsForm.facilityGenerated"
        label="Was data generated at a DOE user facility (JGI, EMSL)? *"
        :rules="[v => (v === true || v === false) || 'You must select if data was generated at a DOE user facility.']"
        :disabled="templateHasData(JGI_TEMPLATE_NAMES) || templateHasData('emsl') || undefined"
        @change="revalidate"
      >
        <v-radio
          label="No"
          :value="false"
          hide-details
        />
        <v-radio
          label="Yes"
          :value="true"
          hide-details
        />
      </v-radio-group>
      <div
        v-if="multiOmicsForm.dataGenerated === true && multiOmicsForm.facilityGenerated"
      >
        <DoeFacility
          @add-award-doi="addAwardDoi"
          @revalidate="revalidate"
        />
      </div>
      <v-radio-group
        v-if="multiOmicsForm.dataGenerated === false"
        v-model="multiOmicsForm.doe"
        label="Are you submitting samples to a DOE user facility (JGI, EMSL)? *"
        :rules="[v => (v === true || v === false) || 'You must select if you are submitting samples to a DOE user facility.']"
        :disabled="templateHasData(JGI_TEMPLATE_NAMES) || templateHasData('emsl') || undefined"
        @change="revalidate"
      >
        <v-radio
          label="No"
          :value="false"
        />
        <v-radio
          label="Yes"
          :value="true"
        />
      </v-radio-group>

      <v-radio-group
        v-if="multiOmicsForm.dataGenerated === false && multiOmicsForm.doe"
        v-model="multiOmicsForm.award"
        label="What kind of project have you been awarded? *"
        :rules="projectAwardValidationRules()"
        class="pb-5"
      >
        <div class="d-flex align-center ga-1">
          <v-radio
            class="flex-0-0"
            value="CSP"
            label="CSP"
          />
          <div>
            (<a
              href="https://jgi.doe.gov/user-programs/program-info/csp-overview/csp-annual-call/"
              target="_blank"
              rel="noopener noreferrer"
            >Community Science Program</a>)
          </div>
        </div>

        <div class="d-flex align-center ga-1">
          <v-radio
            class="flex-0-0"
            value="BERSS"
            label="BERSS"
          />
          <div>
            (<a
              href="https://jgi.doe.gov/user-programs/other-programs/"
              target="_blank"
              rel="noopener noreferrer"
            >Biological and Environmental Research Support Science</a>)
          </div>
        </div>

        <div class="d-flex align-center ga-1">
          <v-radio
            class="flex-0-0"
            value="BRCs"
            label="BRCs"
          />
          <div>
            (<a
              href="https://jgi.doe.gov/user-programs/other-programs/"
              target="_blank"
              rel="noopener noreferrer"
            >Bioenergy Research Centers</a>)
          </div>
        </div>

        <div class="d-flex align-center ga-1">
          <v-radio
            class="flex-0-0"
            value="FICUS"
            label="FICUS"
          />
          <div>
            (<a
              href="https://www.emsl.pnnl.gov/basic/ficus-program/1872"
              target="_blank"
              rel="noopener noreferrer"
            >Facilities Integrating Collaborations for User Science</a>)
          </div>
        </div>

        <div class="d-flex align-center ga-1">
          <v-radio
            class="flex-0-0"
            value="MONet"
            label="MONet"
          />
          <div>
            (<a
              href="https://www.emsl.pnnl.gov/monet"
              target="_blank"
              rel="noopener noreferrer"
            >Molecular Observation Network</a>)
          </div>
        </div>

        <v-radio
          :value="OTHER"
        >
          <template #label>
            <span class="mr-4">Other</span>
            <v-responsive
              class="mx-auto"
              max-width="344"
              min-width="344"
            >
              <v-text-field
                v-model="multiOmicsForm.otherAward"
                class="pa-0 ma-0"
                density="compact"
                hide-details="auto"
                variant="outlined"
                :rules="otherAwardValidationRules()"
              />
            </v-responsive>
          </template>
        </v-radio>
      </v-radio-group>
      <div
        v-if="multiOmicsForm.dataGenerated === false && multiOmicsForm.doe"
        class="pb-4"
      >
        <DoeFacility
          @add-award-doi="addAwardDoi"
          @revalidate="revalidate"
        />
      </div>

      <DataTypes
        v-if="multiOmicsForm.dataGenerated === false && multiOmicsForm.doe === false"
        legend="Which datatypes may be generated later?"
        :show-data-compatibility-questions="false"
        @revalidate="revalidate"
      />
      <DataTypes
        v-if="multiOmicsForm.facilityGenerated === false"
        legend="Which datatypes were generated?"
        :show-data-compatibility-questions="true"
        @revalidate="revalidate"
      />

      <v-alert
        v-if="templateHasData(JGI_TEMPLATE_NAMES) || templateHasData('emsl')"
        type="warning"
      >
        <p class="text-h5">
          Some choices may be disabled
        </p>
        Data types with data present in the Sample Metadata cannot be disabled. To change these templates, return to Sample Metadata and remove data from the tabs you wish to disable.
        You may add unselected data types at any time.
      </v-alert>

      <div
        v-if="selectedFacilities.includes('EMSL') || selectedFacilities.includes('JGI')"
        class="my-4"
      >
        <div
          v-for="(_, i) in multiOmicsForm.awardDois"
          :key="`awardDois${i}`"
          class="d-flex"
        >
          <v-card class="flex-grow-1 pa-4 mb-4">
            <v-row>
              <v-col
                cols="12"
                sm="8"
              >
                <v-text-field
                  v-if="multiOmicsForm.awardDois !== null"
                  v-model="multiOmicsForm.awardDois[i]!.value"
                  label="Award DOI *"
                  :hint="Definitions.doi"
                  :rules="doiValueRules(i)"
                  persistent-hint
                  validate-on-blur
                  variant="outlined"
                  @change="revalidate"
                >
                  <template #message="{ message }">
                    <span v-html="message" />
                  </template>
                </v-text-field>
              </v-col>
              <v-col
                cols="12"
                sm="4"
              >
                <v-select
                  v-if="multiOmicsForm.awardDois !== null"
                  v-model="multiOmicsForm.awardDois[i]!.provider"
                  label="Award DOI Provider *"
                  :hint="Definitions.dataDoiProvider"
                  :items="doiProviderValues"
                  item-title="text"
                  item-value="value"
                  persistent-hint
                  variant="outlined"
                  clearable
                  :rules="doiProviderRules(i)"
                  @change="revalidate"
                >
                  <template #message="{ message }">
                    <span v-html="message" />
                  </template>
                </v-select>
              </v-col>
            </v-row>
          </v-card>
          <v-btn
            v-if="(multiOmicsForm.dataGenerated || selectedFacilities.includes('EMSL') || selectedFacilities.includes('JGI')) && multiOmicsForm.awardDois !== null"
            icon
            variant="plain"
            class="pb-2"
            :disabled="!(selectedFacilities.length < multiOmicsForm.awardDois.length) || formRef?.isDisabled"
            @click="removeAwardDoi(i)"
          >
            <v-icon>mdi-minus-circle</v-icon>
          </v-btn>
        </div>
        <v-checkbox
          v-if="!multiOmicsForm.dataGenerated && (selectedFacilities.includes('EMSL') || selectedFacilities.includes('JGI'))"
          v-model="multiOmicsForm.unknownDoi"
          class="mt-0"
          :label="`I don't know my award DOI`"
          :hint="Definitions.unknownDoi"
          persistent-hint
          @change="revalidate"
        />
      </div>
      <v-btn-grey
        v-if="selectedFacilities.includes('EMSL') || selectedFacilities.includes('JGI')"
        class="mb-4"
        :disabled="formRef?.isDisabled"
        @click="addAwardDoi"
      >
        <v-icon class="pr-1">
          mdi-plus-circle
        </v-icon>
        Add Award DOI
      </v-btn-grey>
    </SubmissionForm>
    <strong>* indicates required field</strong>
    <div class="d-flex mt-5">
      <v-btn-grey
        :to="{ name: 'Study Form' }"
        @click="revalidate"
      >
        <v-icon class="pr-2">
          mdi-arrow-left-circle
        </v-icon>
        Go to Study Form
      </v-btn-grey>
      <v-spacer />
      <v-btn
        color="primary"
        :to="{ name: 'Sample Environment' }"
        @click="revalidate"
      >
        Go to Sample Environment
        <v-icon class="pl-1">
          mdi-arrow-right-circle
        </v-icon>
      </v-btn>
    </div>
  </div>
</template>
