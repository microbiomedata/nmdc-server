<script lang="ts">
import { defineComponent, ref, nextTick } from '@vue/composition-api';
import {
  multiOmicsForm, templateChoiceDisabled,
} from '../store';
import Definitions from '@/definitions';
import SubmissionContextShippingForm from './SubmissionContextShippingForm.vue';

export default defineComponent({
  components: {
    SubmissionContextShippingForm,

  },
  props: {
    label: {
      type: String,
      default: 'Data generated ',
    },
  },

  setup() {
    const doiRequiredRules = () => [(v: string) => {
      const valid = !!v || (!multiOmicsForm.facilityGenerated && multiOmicsForm.dataGenerated) || (multiOmicsForm.unknownDoi && !multiOmicsForm.dataGenerated);
      return valid || 'An award DOI is required if data was or is going to be generated by a DOE facility.';
    }];

    // TODO: Assure dois are being recorded appropriately Also make sure JGI dois are handled correctly
    function addAwardDoi() {
      if (multiOmicsForm.awardDois === null || multiOmicsForm.awardDois.length === 0) {
        multiOmicsForm.awardDois = [''];
      } else {
        multiOmicsForm.awardDois.push('');
      }
    }

    function removeAwardDoi(i: number) {
      if (multiOmicsForm.awardDois === null) {
        multiOmicsForm.awardDois = [''];
      }
      if ((multiOmicsForm.facilities.length < multiOmicsForm.awardDois.length && !multiOmicsForm.dataGenerated) || (multiOmicsForm.facilityGenerated && multiOmicsForm.dataGenerated && multiOmicsForm.awardDois.length > 1) || (!multiOmicsForm.facilityGenerated && multiOmicsForm.dataGenerated)) {
        multiOmicsForm.awardDois.splice(i, 1);
      }
    }

    const formRef = ref();

    const revalidate = () => {
      nextTick(() => formRef.value.validate());
    };

    return {
      addAwardDoi,
      Definitions,
      doiRequiredRules,
      multiOmicsForm,
      removeAwardDoi,
      revalidate,
      templateChoiceDisabled,
    };
  },

});
</script>

<template>
  <div>
    <legend
      class="v-label theme--light mb-2"
      style="font-size: 14px;"
    >
      Which facility?
    </legend>
    <v-checkbox
      v-model="multiOmicsForm.facilities"
      label="EMSL"
      value="EMSL"
      hide-details
      class="mb-2 mt-0"
    />
    <div
      v-if="multiOmicsForm.facilities.includes('EMSL')"
      class="mb-4 ml-4"
    >
      <div
        class="d-flex"
      >
        <v-card
          class="d-flex flex-column grow px-4 mb-4"
          flat
        >
          <v-text-field
            v-if="multiOmicsForm.facilities.includes('EMSL')"
            v-model="multiOmicsForm.studyNumber"
            :rules="[
              v => !!v || 'EMSL Proposal Number is required when processing was done at EMSL',
              v => /^\d{5}$/.test(v) || 'EMSL Proposal Number must be a 5 digit numerical value'
            ]"
            hint="EMSL Proposal Number is required when processing was done at EMSL"
            persistent-hint
            label="EMSL Proposal Number *"
            class="mt-4"
            outlined
            validate-on-blur
            dense
          />
          <div class="d-flex">
            <v-text-field
              v-if="multiOmicsForm.facilities.includes('EMSL')"
              v-model="multiOmicsForm.awardDois"
              label="EMSL Award DOI *"
              :hint="Definitions.doi"
              :rules="doiRequiredRules()"
              persistent-hint
              validate-on-blur
              outlined
              dense
              @change="revalidate"
            >
              <template #message="{ message }">
                <span v-html="message" />
              </template>
            </v-text-field>
          </div>
          <v-checkbox
            v-if="!multiOmicsForm.dataGenerated && multiOmicsForm.facilities.includes('EMSL')"
            v-model="multiOmicsForm.unknownDoi"
            :label="`I don't know my award DOI`"
            :hint="Definitions.unknownDoi"
            persistent-hint
            @change="revalidate"
          />
        </v-card>
      </div>
      <v-radio-group
        v-if="multiOmicsForm.dataGenerated === false && multiOmicsForm.facilities.includes('EMSL')"
        v-model="multiOmicsForm.ship"
        label="Will samples be shipped? *"
        :rules="[v => (v === true || v === false) || 'This field is required']"
        @change="revalidate"
      >
        <v-radio
          label="No"
          :value="false"
        />
        <v-radio
          label="Yes"
          :value="true"
        />
      </v-radio-group>
      <submission-context-shipping-form
        v-if="multiOmicsForm.dataGenerated === false && multiOmicsForm.ship && multiOmicsForm.facilities.includes('EMSL')"
      />
      <div
        class="v-label theme--light mt-6"
        style="font-size: 14px;"
      >
        Data types?
      </div>

      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metaproteome"
        value="mp-emsl"
        :disabled="templateChoiceDisabled"
        hide-details
      />
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metabolome"
        value="mb-emsl"
        :disabled="templateChoiceDisabled"
        hide-details
      />
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Natural Organic Matter (FT-ICR MS)"
        value="nom-emsl"
        :disabled="templateChoiceDisabled"
        hide-details
      />
    </div>
    <v-checkbox
      v-model="multiOmicsForm.facilities"
      label="JGI"
      value="JGI"
      hide-details
      class="mb-2 mt-0"
    />
    <div
      v-if="multiOmicsForm.facilities.includes('JGI')"
      class="my-4 ml-4"
    >
      <div
        :key="`awardDoi${i}`"
        class="d-flex"
      >
        <v-card
          class="d-flex flex-column grow pa-4 mb-4"
          flat
        >
          <v-text-field
            v-if="multiOmicsForm.facilities.includes('JGI')"
            v-model="multiOmicsForm.studyNumber"
            :rules="[ v => !!v || 'JGI Proposal Number is required when processing was done at JGI' ]"
            hint="JGI Proposal Number is required when processing was done at JGI"
            persistent-hint
            label="JGI Proposal Number *"
            class="mt-4"
            outlined
            validate-on-blur
            dense
          />
          <div class="d-flex">
            <v-text-field
              v-if="multiOmicsForm.facilities.includes('JGI')"
              v-model="multiOmicsForm.awardDois"
              label="JGI Award DOI *"
              :hint="Definitions.doi"
              :rules="doiRequiredRules()"
              persistent-hint
              validate-on-blur
              outlined
              dense
              @change="revalidate"
            >
              <template #message="{ message }">
                <span v-html="message" />
              </template>
            </v-text-field>
          </div>
          <v-checkbox
            v-if="!multiOmicsForm.dataGenerated && multiOmicsForm.facilities.includes('JGI')"
            v-model="multiOmicsForm.unknownDoi"
            class="pa-0 mt-4"
            :label="`I don't know my award DOI`"
            :hint="Definitions.unknownDoi"
            persistent-hint
            @change="revalidate"
          />
        </v-card>
      </div>
      <div
        class="v-label theme--light mb-2"
        style="font-size: 14px;"
      >
        Data types?
      </div>
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metagenome"
        value="mg-jgi"
        :disabled="templateChoiceDisabled"
        hide-details
      />
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metagenome (Long Read)"
        value="mg-lr-jgi"
        :disabled="templateChoiceDisabled"
        hide-details
      />
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metatranscriptome"
        value="mt-jgi"
        :disabled="templateChoiceDisabled"
        hide-details
      />
      <v-checkbox
        v-model="multiOmicsForm.omicsProcessingTypes"
        label="Metabolome"
        value="mb-jgi"
        disabled
        hide-details
      />
    </div>
  </div>
</template>
